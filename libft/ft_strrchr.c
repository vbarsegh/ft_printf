#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	if (c < 0)
		return ((char *)s);
	i = ft_strlen(s) - 1;
	if (c == '\0')
		return ((char *)s + i + 1);
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i--;
	}
	return (NULL);
}
